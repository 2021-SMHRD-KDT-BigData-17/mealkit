package domain;

public class Delivery {

	private int deli_seq;
	private int order_seq;
	private String deli_addr;
	private String deli_name;
	private String deli_phone;
	private String deli_company;
	private String deli_date;
	
	public int getDeli_seq() {
		return deli_seq;
	}
	public void setDeli_seq(int deli_seq) {
		this.deli_seq = deli_seq;
	}
	public int getOrder_seq() {
		return order_seq;
	}
	public void setOrder_seq(int order_seq) {
		this.order_seq = order_seq;
	}
	public String getDeli_addr() {
		return deli_addr;
	}
	public void setDeli_addr(String deli_addr) {
		this.deli_addr = deli_addr;
	}
	public String getDeli_name() {
		return deli_name;
	}
	public void setDeli_name(String deli_name) {
		this.deli_name = deli_name;
	}
	public String getDeli_phone() {
		return deli_phone;
	}
	public void setDeli_phone(String deli_phone) {
		this.deli_phone = deli_phone;
	}
	public String getDeli_company() {
		return deli_company;
	}
	public void setDeli_company(String deli_company) {
		this.deli_company = deli_company;
	}
	public String getDeli_date() {
		return deli_date;
	}
	public void setDeli_date(String deli_date) {
		this.deli_date = deli_date;
	}
	
	
}
